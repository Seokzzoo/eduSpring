CREATE TABLE BOARD (
	SEQ 		NUMBER(5) PRIMARY KEY,
	TITLE 		VARCHAR2(200),
	WRITER 		VARCHAR2(20),
	CONTENT 	VARCHAR2(2000),
	REGDATE 	DATE 		DEFAULT CURRENT_DATE,
	CNT 		NUMBER(5)	DEFAULT 0
);

CREATE TABLE USERS (
	ID 			VARCHAR2(12) PRIMARY KEY,
	PASSWORD 	VARCHAR2(12),
	NAME 		VARCHAR2(50),
	ROLE 		VARCHAR2(5) DEFAULT 'USER'
);